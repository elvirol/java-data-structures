package com.github.elvirol.datastructures.Map.exercise3;

public class Purchase {
	
	private String name;
	private int amount;
	
	public Purchase(String name, int amount) {
		this.name = name;
		this.amount = amount;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAmount() {
		return amount;
	}
}
